let choice = 'x';
switch(choice) {
    case 1:
        console.log("case 1");
        
    case 2:
        console.log("case 2");
        
    case 'y':
        console.log("case y");
        
    case 'x':
        console.log("case x");
        
    case undefined:
        console.log(undefined);
        
    case null:
        console.log(null);
        
}